func TestScanRejectsSupportedSymlink(t *testing.T) {
	root := t.TempDir()
	writeCommentDebtFile(t, root, "target.go", `package sample

// TODO: outside normal file boundary
func Run() {}
`)
	link := filepath.Join(root, "internal", "sample", "link.go")
	if err := os.MkdirAll(filepath.Dir(link), 0o750); err != nil {
		t.Fatalf("mkdir %s: %v", filepath.Dir(link), err)
	}
	if err := os.Symlink(filepath.Join(root, "target.go"), link); err != nil {
		t.Skipf("symlink unavailable: %v", err)
	}

	_, err := scanRoot(root)
	if err == nil {
		t.Fatal("scanRoot passed unexpectedly")
	}
	if !strings.Contains(err.Error(), "internal/sample/link.go: source path must be a regular file") {
		t.Fatalf("error = %v, want regular file rejection", err)
	}
}

