func TestRunReportsViolations(t *testing.T) {
	root := t.TempDir()
	writeCommentDebtFile(t, root, "internal/sample/sample.go", `package sample

// TODO: untracked
func Run() {}
`)

	var stdout bytes.Buffer
	err := run(root, &stdout)
	if err == nil {
		t.Fatal("run passed unexpectedly")
	}
	for _, want := range []string{
		"[comment-debt] untracked source comment debt:",
		"internal/sample/sample.go:3: TODO comment must reference an issue or ADR",
	} {
		if !strings.Contains(stdout.String(), want) {
			t.Fatalf("stdout = %q, want %q", stdout.String(), want)
		}
	}
}

func writeCommentDebtFile(t *testing.T, root, name, body string) {
	t.Helper()
	path := filepath.Join(root, filepath.FromSlash(name))
	if err := os.MkdirAll(filepath.Dir(path), 0o750); err != nil {
		t.Fatalf("mkdir %s: %v", filepath.Dir(path), err)
	}
	if err := os.WriteFile(path, []byte(body), 0o600); err != nil {
		t.Fatalf("write %s: %v", path, err)
	}
}
