func TestScanGoCommentsIgnoreStringLiterals(t *testing.T) {
	root := t.TempDir()
	writeCommentDebtFile(t, root, "cmd/sample/main.go", `package main

var fixture = "// TODO: call langchain here."

// TODO(#123): tracked item
func main() {}
`)

	result, err := scanRoot(root)
	if err != nil {
		t.Fatalf("scanRoot: %v", err)
	}
	if len(result.Violations) != 0 {
		t.Fatalf("violations = %+v, want none", result.Violations)
	}
}

func TestScanRejectsUntrackedGoComment(t *testing.T) {
	root := t.TempDir()
	writeCommentDebtFile(t, root, "internal/sample/sample.go", `package sample

// FIXME: wire the real boundary
func Run() {}
`)

	result, err := scanRoot(root)
	if err != nil {
		t.Fatalf("scanRoot: %v", err)
	}
	if len(result.Violations) != 1 {
		t.Fatalf("violations = %+v, want one", result.Violations)
	}
	got := result.Violations[0]
	if got.Path != "internal/sample/sample.go" || got.Line != 3 || got.Marker != "FIXME" {
		t.Fatalf("violation = %+v, want internal/sample/sample.go:3 FIXME", got)
	}
}

func TestAcceptedTrackingReferences(t *testing.T) {
	for _, tc := range []struct {
		name string
		ref  string
	}{
		{name: "issue shorthand", ref: "#123"},
		{name: "github issue url", ref: "https://github.com/openclarion/openclarion/issues/123"},
		{name: "github pull url", ref: "https://github.com/openclarion/openclarion/pull/123"},
		{name: "adr id", ref: "ADR-0001"},
		{name: "adr path", ref: "docs/adr/ADR-0001-postgresql-single-source-of-truth.md"},
	} {
		t.Run(tc.name, func(t *testing.T) {
			root := t.TempDir()
			writeCommentDebtFile(t, root, "internal/sample/sample.go", `package sample

// HACK: tracked by `+tc.ref+`
func Run() {}
`)
			result, err := scanRoot(root)
			if err != nil {
				t.Fatalf("scanRoot: %v", err)
			}
			if len(result.Violations) != 0 {
				t.Fatalf("violations = %+v, want none", result.Violations)
			}
		})
	}
}

