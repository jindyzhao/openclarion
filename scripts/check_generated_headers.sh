#!/usr/bin/env bash
# Validate generated-file headers for committed generator outputs.

set -euo pipefail

cd "$(dirname "$0")/.."

failed=0
checked=0

check_first_line() {
  local file="$1"
  local expected="$2"

  if [[ ! -f "$file" ]]; then
    return 0
  fi

  checked=$((checked + 1))
  local first_line
  IFS= read -r first_line < "$file" || first_line=""
  if [[ "$first_line" != "$expected" ]]; then
    echo "[generated-headers] $file missing expected generated header:" >&2
    echo "  expected first line: $expected" >&2
    failed=1
  fi
}

check_contains_near_top() {
  local file="$1"
  local marker="$2"

  if [[ ! -f "$file" ]]; then
    return 0
  fi

  if ! sed -n '1,8p' "$file" | grep -Fq "$marker"; then
    echo "[generated-headers] $file missing expected generated marker near top:" >&2
    echo "  expected marker: $marker" >&2
    failed=1
  fi
}

check_first_line "api/openapi.gen.go" "// Code generated by oapi-codegen; DO NOT EDIT."

if [[ -d internal/persistence/ent ]]; then
  mapfile -t ent_files < <(find internal/persistence/ent \
    -type f -name '*.go' \
    ! -path 'internal/persistence/ent/schema/*' \
    ! -name 'generate.go' \
    | sort)

  for ent_file in "${ent_files[@]}"; do
    check_first_line "$ent_file" "// Code generated by ent, DO NOT EDIT."
  done
fi

if [[ -f web/src/lib/api/openapi.ts ]]; then
  checked=$((checked + 1))
  check_contains_near_top "web/src/lib/api/openapi.ts" "This file was auto-generated by openapi-typescript."
  check_contains_near_top "web/src/lib/api/openapi.ts" "Do not make direct changes to the file."
fi

if [[ $checked -eq 0 ]]; then
  echo "[generated-headers] no generated files found; skipping."
  exit 0
fi

if [[ $failed -ne 0 ]]; then
  echo "" >&2
  echo "Fix: regenerate the affected file instead of hand-editing or replacing the generated header." >&2
  exit 1
fi

echo "[generated-headers] OK ($checked files checked)"
